            val target = 0 - nums[i]
            for (j in nums.indices) {
                val diff = target - nums[j]
                if (hashmap.containsKey(diff) && i != j && hashmap[diff] != i && hashmap[diff] != j) {
                    val li = listOf(nums[i], nums[j], nums[hashmap[diff]!!]).sorted()
                    hashset.add(li)
                }
                hashmap[nums[j]] = j
            }
        }
        return ArrayList(hashset)
    }

